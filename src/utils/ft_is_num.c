#include "philo.h"

int	ft_is_num(char *str)
{
	int	i;

	i = 0;
	while (str[i] != '\n')
	{
		if (str[i] < '0' || str[i] > '9')
			return (0);
		i++;
	}
	if (i == 0)
		return (-1);
	return (1);
}
